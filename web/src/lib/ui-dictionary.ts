/**
 * UI display-label dictionary, grouped by user-manual topic.
 *
 * Each element's `id` is the i18n key from translations.ts, typed as
 * TranslationKey so a renamed or deleted key fails the build here instead of
 * silently breaking a manual link. label_en / label_th mirror the
 * translations for label search (label -> category -> manual section).
 *
 * guide_en / guide_th hold the "UI Mini Guide" — a one-line explanation of
 * each label shown in the guide page's expandable label table. These are the
 * authored defaults; admins can override them at runtime via
 * /api/guide/overrides (stored in .data/ui-guide-overrides.json), and the
 * guide page shows override text when present.
 */

